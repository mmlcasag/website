CREATE TABLE WEB_PROD_CARACTERISTICA
(
                PCAR_ID decimal(10,0) PRIMARY KEY NOT NULL,
                PTPC_ID decimal(10,0),
                PAV_ID decimal(10,0),
                DESCRICAO varchar2(255),
                CONSTRAINT FK_PTPC_ID FOREIGN KEY(PTPC_ID) REFERENCES WEB_PROD_TP_CARACTERISTICA(PTPC_ID),
                CONSTRAINT FK_PAV_ID FOREIGN KEY(PAV_ID) REFERENCES WEB_PROD_AVALIACOES(PAV_ID)
);